package loopDemo;

public class Main1 {

	public static void main(String[] args) {
		
	for(int i =1;i<=20;i++) {
		System.out.println(i);
	}System.out.println("Döngü bitti.");
		
	int i =1;
	while (i<=10) {
		
		System.out.println(i);	
		i ++;
	}
	System.out.println("Döngü bitti.");

	
	int a =1;
	do {
		System.out.println(a);
	a++;
	}while(a<10);
	
	System.out.println("Döngü bitti.");
	}

}
